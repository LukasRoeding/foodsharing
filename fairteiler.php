        <div class="grid-text"><h1 class = "section-header">Was ist ein Fairteiler?</h1>
            Ein Fair-Teiler ist ein Ort, zu dem alle Menschen Lebensmittel bringen und kostenlos von dort mitnehmen dürfen. 
            Foodsaver können gerettete Lebensmittel zu einem Fair-Teiler bringen, die noch zur Weitergabe geeignet sind. 
            Alle anderen Menschen können ebenfalls Lebensmittel dorthin bringen, die sie zum Beispiel zu Hause oder nach Veranstaltungen übrig 
            haben und nicht mehr verbrauchen wollen oder können. Herausnehmen dürfen das dort bereitgestellte Essen alle Menschen, 
            ohne irgendwelche Vorraussetzungen erfüllen zu müssen. Betrieben wird ein Fair-Teiler von dem foodsharing-Bezirk, in dem er steht. 
            Gerettete Lebensmittel können auch an Abgabestellen geliefert werden. 
            Eine Abgabestelle unterscheidet sich von einem Fair-Teiler dadurch, dass sie nicht von foodsharing betrieben wird, 
            sondern von einer anderen Organisation oder einer Privatperson. 
            <h1 class = "section-header">Regeln</h1>
            Es ist äußerst wichtig, dass Fairteiler regelmäßig(alle 2 Tage) gereinigt werden. Falls du bei einer Abholung feststellst, 
            dass ein Fairteiler dreckig ist, so reinige ihn bitte. Passende Putzmittel werden dafür vor Ort sein. 
            Wenn sich Lebensmittel im Fairteiler befinden, die nicht mehr essbar sind, entsorge diese. Nur so lässt sich garantieren, 
            dass die Fairteiler auch weiterhin zur Verfügung stehen.
            <br><br>
            Diese Lebensmittel dürfen <b>NICHT</b> in einem Fairteiler weitergegeben werden!
            <ul>
                <li>Lebensmittel nach Ablauf des Verbrauchsdatums dürfen auf keinen Fall weitergegeben werden. Sie stellen eine Gesundheitsgefahr dar.</li>
                <li>Lebensmittel mit MHD oder Verbrauchdatums, bei denen die (innerste) Verpackung geöffnet ist, gehören ebenfalls nicht in einen Fair-Teiler.</li>
                <li>Bei Kühlwaren muss die Kühlkette eingehalten werden und sie dürfen nur in Kühlschränke gelegt werden. Wenn sie nicht ununterbrochen in einer Kühlbox oder Kühltasche mit Kühlelementen transportiert bzw. gelagert wurden, gehören sie nicht in einen Fair-Teiler.</li>
                <li>Einige Risikonahrungsmittel (Hackfleisch, roher Fisch, Produkte mit Rohmilch oder rohem Ei) dürfen wegen der potenziellen Gesundheitsgefahr, die sie darstellen, nicht über Fair-Teiler geteilt werden!</li>
                <li>Auch selbstgesammelte Pilze und Alkohol (auch in Form von Pralinen etc.) sowie Energy-Drinks sind nicht erlaubt.</li>
            </ul>

