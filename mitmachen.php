        <div class="grid-text"><h1 class = "section-header">Mitmachen</h1>
        Wenn du vor hast bei foodsharing aktiv zu werden, fängst du damit am besten bei dir zu Hause an. 
        <a class="green" target="_blank" href="https://www.umweltbundesamt.de/themen/wider-die-verschwendung">In deutschen Haushalten wird jedes achte Lebensmittel einfach weggeschmissen.</a> 
        Das sind pro Haushalt 82 Kilogramm weggeschmissene Lebensmittel.<br>
        Achte beim Einkaufen darauf, wirklich nur die Lebensmittel einzukaufen, die du auch brauchst. 
        Falls du doch einmal zu viele Lebensmittel eingekauft hast, frag doch mal bei den Nachbarn, ob die sie gebrauchen können.<br>
        In Dortmund gibt es auch eine foodsharing <a class="green" target="_blank" href="https://www.facebook.com/groups/foodsharingDO/">Facebook-Gruppe</a>. 
        Dort könntest du deine überschüssigen Lebensmittel posten und falls jemand Interesse an deinem Essen hat, wird sich die Person bei dir melden.<br>
        Eine weitere Möglichkeit Lebensmittel zu verteilen, ist die Anmeldung als Foodsharer auf <a class="green" target="_blank" href="https://foodsharing.de/">foodsharing.de</a>. 
        Als Foodsharer hast du die Möglichkeit einen digitalen Essenskorb mit deinen Lebensmitteln zu erstellen. Dieser erscheint dann auf der eigenen Karte von foodsharing 
        und andere Lebensmittelretter können sich dann bei dir melden.<br>
        Alternativ könntest du auch die Lebensmittel mit einem <a class="green" target="_blank" href="fairteiler.php">Fairteiler</a> verteilen.<br>
        Wenn du dich noch mehr einbringen willst, kannst du auch Foodsaver werden. Damit erhälst du die Möglichkeit, bei Betrieben Lebensmittel zu retten und diese zu verteilen.<br>
        Ein weiterer wichtiger Aufgabenbereich von Foodsavern ist die Aufklärungsarbeit. Dafür betreiben wir zum Beispiel auf Veranstaltungen oder Messen Infostände rund um das Thema Lebensmittelverschwendung. 
        Dabei klären wir über die Lebensmittelverschwendung, deren Folgen und möglichen Gegenmaßnahmen auf. Als Foodsaver wird von dir erwartet, dass du dich an der Aufklärungsarbeit beteiligst. 
        Dies muss nicht ausschließlich durch die Teilnahme an einem Infostand passieren. Du könntest auch einen Workshop zum fermentieren geben um, Lebensmittel länger haltbar zu machen oder einen Vortrag in einer Schule halten. 
        Effektiv kannst du dich in dem Bereich so einbringen, wie du es für dich am besten hälst. Falls du Ideen hast, kannst du damit immer gerne zum monatlichen Gruppenabend kommen und diese vorstellen.
        <h1 class = "section-header">Wie werde ich Foodsaver?</h1>
        Um in Dortmund als Foodsaver aktiv zu werden, musst du fünf Bedingungen erfüllen.
        <ol>
                        <li>Melde dich auf <a class="green" target="_blank" href="https://foodsharing.de/">foodsharing.de</a> an und bestehe das Foodsaver-Quiz.</li>
                        <li>Absolviere deine drei Einführungsabholungen, bei der wir dir zeigen, wie unsere Rettungen bei Betrieben funktionieren.</li>
                        <li>Führe eine Hygieneschulung durch (beim Gesundheitsamt oder kostenlos <a class="green" target="_blank" href="https://www.metro.de/service/etraining/haccp">hier</a>).</li>
                        <li>Nimm an einer sozialen Veranstaltung (Infoabend, gemeinsames Kochen, Aushelfen beim Infostand auf Veranstaltungen, etc.) teil.
                        So können wir uns gegenseitig kennenlernen.</li>
                        <li>Werde Mitglied im foodsharing Dortmund e.V.. Das kostet dich nichts und du bist damit bei den Abholungen und Veranstaltungen versichert. 
                        Den Mitgliedsantrag, sowie Satzung und Geschäftsordnung findest du unter 
                        <a class="green" target="_blank" href="formulare.php">Formulare</a>.
                        </li>
